package variable;

public class Type2 {
	public static void main(String[] args) {
		// 변수를 활용해서
		// 이름, 출생년도, 키
		
		// 예시의 ooo을 변수로 사용		
		/*
		 이름 : ooo
		 출생년도 : oooo년
		 키 : ooo.oocm
		*/
		
		String name = "홀길동";
		int birth = 2000;
		double height = 175.5;
						
		System.out.println("이름 : "+name);
		System.out.println("출생년도 : "+birth);
		System.out.println("키 : "+height+"cm");
		
	}
}
